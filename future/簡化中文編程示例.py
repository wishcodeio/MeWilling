class 中文編程:
    """簡化的中文編程解釋器"""
    
    def __init__(self):
        self.變量 = {}
        
    def 賦值(self, 變量名, 值):
        """中文賦值操作"""
        self.變量[變量名] = 值
        return 值
    
    def 取值(self, 變量名):
        """獲取變量值"""
        return self.變量.get(變量名, None)
    
    def 打印(self, *內容):
        """中文打印函數"""
        print(*內容)
    
    def 如果(self, 條件, 真值操作, 假值操作=None):
        """中文條件判斷"""
        if 條件:
            return 真值操作() if callable(真值操作) else 真值操作
        elif 假值操作:
            return 假值操作() if callable(假值操作) else 假值操作
    
    def 循環(self, 次數, 操作):
        """中文循環"""
        結果 = []
        for i in range(次數):
            if callable(操作):
                結果.append(操作(i))
            else:
                結果.append(操作)
        return 結果
    
    def 遍歷(self, 列表, 操作):
        """中文遍歷"""
        結果 = []
        for 項目 in 列表:
            if callable(操作):
                結果.append(操作(項目))
            else:
                結果.append(操作)
        return 結果

class 願語編程(中文編程):
    """願語編程擴展"""
    
    def __init__(self):
        super().__init__()
        self.願望池 = []
        self.實現記錄 = []
    
    def 許願(self, 願望):
        """許下願望"""
        self.願望池.append(願望)
        self.打印(f"✨ 願望已許下：{願望}")
        return 願望
    
    def 實現願望(self, 願望=None):
        """實現願望"""
        if 願望 is None and self.願望池:
            願望 = self.願望池.pop(0)
        
        if 願望:
            self.實現記錄.append(願望)
            self.打印(f"🌟 願望正在實現：{願望}")
            return f"已實現：{願望}"
        else:
            self.打印("💫 暫無待實現的願望")
            return None
    
    def 創造美好(self, 美好事物):
        """創造美好"""
        self.打印(f"🌈 正在創造：{美好事物}")
        return f"✨ {美好事物} 已創造完成"
    
    def 傳遞愛意(self, 接收者="世界"):
        """傳遞愛意"""
        愛意消息 = f"💖 向 {接收者} 傳遞滿滿的愛意"
        self.打印(愛意消息)
        return 愛意消息
    
    def 點亮希望(self, 希望內容="光明的未來"):
        """點亮希望"""
        希望消息 = f"🕯️ 為 {希望內容} 點亮希望之光"
        self.打印(希望消息)
        return 希望消息
    
    def 連接心靈(self, 心靈1, 心靈2):
        """連接心靈"""
        連接消息 = f"🔗 {心靈1} 與 {心靈2} 心靈相通"
        self.打印(連接消息)
        return 連接消息
    
    def 計算愛的力量(self, 愛心數量):
        """計算愛的力量"""
        力量值 = 愛心數量 * 愛心數量  # 愛的力量是平方增長的
        self.打印(f"💝 {愛心數量} 顆愛心產生了 {力量值} 單位的愛的力量")
        return 力量值
    
    def 傳播正能量(self, 能量等級):
        """傳播正能量"""
        if 能量等級 > 10:
            消息 = "🌟 超強正能量正在傳播！"
        elif 能量等級 > 5:
            消息 = "✨ 正能量正在穩定傳播"
        else:
            消息 = "💫 正能量開始萌芽"
        
        self.打印(消息)
        return 消息

